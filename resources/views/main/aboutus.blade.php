<div class="context">
    <p><strong>Who we are</strong></p>
    <p>The Commission on Human Rights (CHR) is an independent 
        National Human Rights Institution (NHRI) created under 
        the 1987 Philippine Constitution, established on 05 
        May 1987 by virtue of Executive Order No. 163.</p>
    <p>The Commission is mandated to conduct investigations 
        on human rights violations against marginalized and 
        vulnerable sectors of the society, involving civil 
        and political rights.</p>
    <p>CHR is an “A” accredited NHRI, fully complying with 
        the Paris Principles adopted by the United Nations 
        General Assembly in 1995. As an NHRI, the Commission 
        upholds six fundamental characteristics — independence, 
        pluralism, broad mandate, transparency, accessibility, 
        and operational efficiency.</p>
    <p>The Commission commits to deliver prompt, responsive, 
        accessible, and excellent public ser vice for the protection 
        and promotion of human rights in accordance with universal 
        human rights principles and standards.</p>
</div>
<div class="context">
   <h1>HISTORY</h1><br>
    <p>The CHR was created as a response to the atrocities committed
         during Martial Law. When the 1987 Philippine Constitution was 
         drafted, Article XIII on Social Justice and Human Rights clearly
          defined the creation of the Commission.</p>
    <p>“There is hereby created an independent office called The Commission 
        on Human Rights… (to) investigate, on its own or on complaint by 
        any party, all forms of human rights violations involving civil and 
        political rights …” (Sec. 17-18, Art. XIII, Philippine Constitution)</p>
    <p>“I, Corazon C. Aquino, President of the Philippines… do hereby (declare) 
        the Commission on Human Rights as provided under Article XIII of the 1987 
        Constitution to be now in existence…” (Executive Order No. 163)</p>
    <p>The 1987 Philippine Constitution primarily gave CHR the mandate to 
        protect and promote the rights and dignity of every human being in
         the country. The State values the dignity of every human person and 
         guarantees full respect for human rights.(Sec. 11, Art. II, Philippine 
         Constitution)</p>
    <p>The Congress shall give highest priority to the enactment of measures 
        that protect and enhance the right of all the people to human dignity, 
        reduce social, economic and political inequalities, and remove cultural 
        inequalities by equitably diffusing wealth and political power for the 
        common good. (Sec. 1, Art. XIII, Philippine Constitution)</p>
</div>
<div class="context">
    <h1>FUNCTIONS OF THE CHR</h1><br>
    <p>
    Under Section 18, Article XIII of the 1987 Constitution, the Commission on 
    Human Rights shall have the following powers and functions:</p><br>
    <p>
        <li>Investigate, on its own or on complaint by any party, all forms of 
            human rights violations involving civil and political rights;</li>
        <li>Adopt its operational guidelines and rules of procedure, and cite 
            for contempt for violations thereof in accordance with the Rules of Court;</li>
        <li>Provide appropriate legal measures for the protection of human rights 
            of all persons within the Philippines, as well as Filipinos residing 
            abroad, and provide for preventive measures and legal aid services to 
            the under-privileged whose human rights have been violated or need protection;</li>
        <li>Exercise visitorial powers over jails, prisons, or detention facilities;</li>
        <li>Establish a continuing program of research, education, and information to 
            enhance respect for the primacy of human rights;</li>
        <li>Recommend to Congress effective measures to promote human rights and to provide 
            for compensation to victims of violations of human rights, or their families;</li>
        <li>Monitor the Philippine Government’s compliance with international treaty obligation on human rights;</li>
        <li>Grant immunity from prosecution to any person whose testimony or whose 
            possession of documents or other evidence is necessary or convenient to 
            determine the truth in any investigation conducted by it or under its authority;</li>
        <li>Request the Assistance of any department, bureau, office, or agency in the performance of its functions;</li>
        <li>Appoint its officers and employees in accordance with law; and</li>
        <li>Perform such other duties and functions as may be provided by law.</li>
    </p>
</div>
